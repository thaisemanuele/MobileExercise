package com.app.jobapplication.models;

/**
 * Class that represents a transaction
 * example: 
 *	Apple Store (store), 
 *	R$(currency), 
 *	2600,00(value),
 * 	false(chargeback was not submitted), 
 * 	eletronicos(category)
 * 
 * @author Thais
 */
public class Transaction {
	
	private String store;
	private String currency;
	private String value;
	private String category;
	private Boolean chargeback;
	
	
	public Transaction(String store, String currency, String value, Boolean chargeback, String category) {
		super();
		this.store = store;
		this.currency = currency;
		this.value = value;
		this.category = category;
		this.chargeback = chargeback;
	}

	public void setStore(String store) {
		this.store = store;
	}
	public String getStore() {
		return store;
	}
	public void setTitle(String title) {
		this.store = title;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public Boolean getChargeback() {
		return chargeback;
	}
	public void setChargeback(Boolean chargeback) {
		this.chargeback = chargeback;
	}
}
